package org.example;

public class Calculator {
    public int add(int a, int b){
        return a + b;
    }

    public int minus(int a, int b){
        return a - b;
    }

    public double mult(double a, double b){
        return a * b;
    }

    public double del(double a, double b){
        return a / b;
    }
}
